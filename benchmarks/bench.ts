#!/usr/bin/env bun
// kittens-crew benchmark. Deterministic, reproducible, no model calls, no faked
// numbers. Measures two things, renders an SVG chart, and rewrites the README
// between <!-- BENCH:START --> / <!-- BENCH:END --> markers.
//
//   1. Spec compression — the SAME spec as caveman SPEC.md vs a prose PRD.
//   2. Standing context cost — tokens each tool injects every session, read from
//      its real instruction file on disk (N/A if that plugin isn't installed).
//
// Run:  cd benchmarks && bun install && bun bench
import { encode } from "gpt-tokenizer/encoding/o200k_base"; // GPT-4o family; proxy for Claude
import { readFileSync, existsSync } from "node:fs";
import { join } from "node:path";
import { homedir } from "node:os";

const tok = (s: string) => encode(s).length;
const here = new URL(".", import.meta.url).pathname;
const root = join(here, "..");
const readMaybe = (p: string) => (existsSync(p) ? readFileSync(p, "utf8") : null);
const firstGlob = (pattern: string) => {
  const g = new Bun.Glob(pattern);
  for (const m of g.scanSync({ absolute: true })) return m;
  return null;
};

// 1. COMPRESSION ------------------------------------------------------------
const prose = readFileSync(join(here, "fixtures/spec.prose.md"), "utf8");
const cave = readFileSync(join(here, "fixtures/spec.caveman.md"), "utf8");
const pT = tok(prose),
  cT = tok(cave);
const savedPct = Math.round(((pT - cT) / pT) * 100);

// 2. STANDING CONTEXT COST --------------------------------------------------
// What each tool adds to context every session. Real local files; N/A if absent.
const cacheGlob = (p: string) => firstGlob(join(homedir(), ".claude/plugins/cache", p));
const sources: { tool: string; file: string | null }[] = [
  { tool: "pure Claude Code", file: null }, // baseline: injects nothing
  { tool: "caveman (compress skill)", file: cacheGlob("caveman/caveman/*/caveman/SKILL.md") },
  { tool: "ponytail (full ruleset)", file: cacheGlob("ponytail/ponytail/*/skills/ponytail/SKILL.md") },
  { tool: "kittens-crew (persona)", file: join(root, "AGENTS.md") },
];
const overhead = sources.map(({ tool, file }) => {
  const text = file === null ? "" : readMaybe(file);
  return { tool, tokens: text === null ? null : tok(text) };
});

// SVG CHART — standing context cost across tools (the cross-tool comparison) ---
function barChart(): string {
  const W = 760, rowH = 50, pad = 18, labelW = 200;
  const bars = overhead.map((o) => ({
    name: o.tool.replace(/ \(.*\)/, ""),
    val: o.tokens,
    fill: o.tool.startsWith("kittens") ? "#e6007a" : o.tool.startsWith("pure") ? "#5b606b" : "#9aa0a6",
    me: o.tool.startsWith("kittens"),
  }));
  const max = Math.max(...bars.map((b) => b.val ?? 0)) || 1;
  const H = pad * 2 + bars.length * rowH + 40;
  const scale = (v: number) => ((W - labelW - pad * 2 - 60) * v) / max;
  const rows = bars
    .map((b, i) => {
      const y = pad + 30 + i * rowH;
      const v = b.val ?? 0;
      const w = Math.max(2, scale(v));
      const lbl = b.val === null ? "n/a" : `${v} tok`;
      return `
    <text x="${pad}" y="${y + 21}" font-size="15" fill="${b.me ? "#e6007a" : "#e8eaed"}" font-weight="${b.me ? 700 : 400}" font-family="ui-monospace,monospace">${b.name}${b.me ? " ★" : ""}</text>
    <rect x="${labelW}" y="${y}" width="${w}" height="30" rx="5" fill="${b.fill}"/>
    <text x="${labelW + w + 10}" y="${y + 21}" font-size="14" fill="#e8eaed" font-family="ui-monospace,monospace">${lbl}</text>`;
    })
    .join("");
  return `<svg xmlns="http://www.w3.org/2000/svg" width="${W}" height="${H}" viewBox="0 0 ${W} ${H}" role="img">
  <rect width="${W}" height="${H}" rx="12" fill="#1b1d22"/>
  <text x="${pad}" y="${pad + 14}" font-size="15" font-weight="700" fill="#15d6c6" font-family="ui-monospace,monospace">standing context cost per session · lower is leaner (o200k)</text>${rows}
</svg>`;
}
await Bun.write(join(here, "chart.svg"), barChart());
// GitHub is unreliable rendering SVG via <img>; rasterize to PNG if a converter exists.
function rasterize(svg: string, png: string): boolean {
  for (const cmd of [["inkscape", svg, "--export-type=png", `--export-filename=${png}`, "-w", "1280"], ["rsvg-convert", "-w", "1280", "-o", png, svg]]) {
    try { if (Bun.spawnSync(cmd).success) return true; } catch {}
  }
  return false;
}
const chartPng = rasterize(join(here, "chart.svg"), join(here, "chart.png"));
const chartImg = chartPng ? "./benchmarks/chart.png" : "./benchmarks/chart.svg";

// MARKDOWN BLOCK ------------------------------------------------------------
const naRow = (o: { tool: string; tokens: number | null }) =>
  `| ${o.tool} | ${o.tokens === null ? "_(not installed)_" : o.tokens === 0 ? "0" : o.tokens} |`;
const block = `<!-- BENCH:START -->
<!-- generated by \`bun bench\` — do not edit by hand -->

<p align="center"><img src="${chartImg}" alt="standing context cost per session" width="680"/></p>

**Standing context cost** — what each tool injects into context every session
(lower = leaner; measured from each tool's real instruction file). kittens-crew
carries a whole spec pipeline yet stays leaner than caveman or ponytail:

| tool | tokens / session |
|---|---:|
${overhead.map(naRow).join("\n")}

**Spec compression** — the same spec, two encodings (tokenizer \`o200k_base\`):

| encoding | tokens | chars | lines |
|---|---:|---:|---:|
| prose PRD | ${pT} | ${prose.length} | ${prose.split("\n").length} |
| **caveman SPEC.md** | **${cT}** | ${cave.length} | ${cave.split("\n").length} |

**${savedPct}% fewer tokens for the same spec**, and the spec reloads on every command — the saving recurs each call.

_Cross-tool rows need that plugin installed locally to measure. Token-per-task pass-rate comparisons need real model runs — method in [\`benchmarks/\`](./benchmarks/), not faked._
<!-- BENCH:END -->`;

// REWRITE README ------------------------------------------------------------
const readmePath = join(root, "README.md");
const readme = readFileSync(readmePath, "utf8");
const re = /<!-- BENCH:START -->[\s\S]*?<!-- BENCH:END -->/;
if (re.test(readme)) {
  await Bun.write(readmePath, readme.replace(re, block));
  console.log("README benchmark block updated.");
} else {
  console.log("No BENCH markers in README. Block to paste:\n\n" + block);
}

// CONSOLE -------------------------------------------------------------------
console.log(`\ncompression: ${pT} → ${cT} tok  (${savedPct}% saved)`);
console.log("standing cost / session:");
for (const o of overhead) console.log(`  ${o.tool.padEnd(28)} ${o.tokens === null ? "n/a" : o.tokens}`);
