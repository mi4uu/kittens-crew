#!/usr/bin/env bun
// Reads results.json from run.ts, renders a grouped bar chart (every metric as a
// % of the no-skill baseline) and injects it + a table into the README between
// <!-- AGENTIC:START --> / <!-- AGENTIC:END -->. Run after run.ts.
import { readFileSync, writeFileSync, existsSync } from "node:fs";
import { join } from "node:path";

const HERE = new URL(".", import.meta.url).pathname;
const ROOT = join(HERE, "..", "..");
const resPath = join(HERE, "results.json");
if (!existsSync(resPath)) { console.error("no results.json — run `bun run.ts` first"); process.exit(1); }
const R = JSON.parse(readFileSync(resPath, "utf8"));

const COLORS: Record<string, string> = {
  baseline: "#9aa0a6",
  "kittens-crew": "#e6007a",
  caveman: "#e8a33d",
  ponytail: "#39b54a",
};
const METRICS = [
  { key: "loc", label: "LOC", base: R.base.loc },
  { key: "tokens", label: "tokens", base: R.base.tokens },
  { key: "cost", label: "cost", base: `$${R.base.cost.toFixed(3)}` },
  { key: "sec", label: "time", base: `${R.base.sec.toFixed(0)}s` },
];
const arms: any[] = R.arms;

// SVG grouped bars ----------------------------------------------------------
const W = 760, H = 420, padL = 48, padB = 70, padT = 60, padR = 20;
const plotW = W - padL - padR, plotH = H - padT - padB;
const maxPct = Math.max(120, ...arms.flatMap((a) => METRICS.map((m) => a.pct[m.key])));
const y = (pct: number) => padT + plotH - (plotH * pct) / maxPct;
const groupW = plotW / METRICS.length;
const barW = Math.min(38, (groupW - 20) / arms.length);

let bars = "";
METRICS.forEach((m, gi) => {
  const gx = padL + gi * groupW + (groupW - barW * arms.length) / 2;
  arms.forEach((a, ai) => {
    const x = gx + ai * barW;
    const pct = a.pct[m.key];
    const yy = y(pct);
    bars += `<rect x="${x.toFixed(1)}" y="${yy.toFixed(1)}" width="${(barW - 3).toFixed(1)}" height="${(padT + plotH - yy).toFixed(1)}" rx="3" fill="${COLORS[a.arm] || "#888"}"/>`;
    bars += `<text x="${(x + (barW - 3) / 2).toFixed(1)}" y="${(yy - 5).toFixed(1)}" font-size="11" fill="${COLORS[a.arm] || "#888"}" text-anchor="middle" font-family="ui-monospace,monospace">${pct}%</text>`;
  });
  bars += `<text x="${(padL + gi * groupW + groupW / 2).toFixed(1)}" y="${H - padB + 22}" font-size="14" fill="#e8eaed" text-anchor="middle" font-family="ui-monospace,monospace">${m.label}</text>`;
  bars += `<text x="${(padL + gi * groupW + groupW / 2).toFixed(1)}" y="${H - padB + 38}" font-size="10" fill="#9aa0a6" text-anchor="middle" font-family="ui-monospace,monospace">base ${m.base}</text>`;
});
// 100% baseline line
const y100 = y(100);
const grid = `<line x1="${padL}" y1="${y100}" x2="${W - padR}" y2="${y100}" stroke="#555" stroke-dasharray="4 4"/><text x="${padL - 6}" y="${y100 + 4}" font-size="10" fill="#9aa0a6" text-anchor="end" font-family="ui-monospace,monospace">100%</text>`;
const legend = arms
  .map((a, i) => `<rect x="${padL + i * 150}" y="32" width="12" height="12" rx="2" fill="${COLORS[a.arm] || "#888"}"/><text x="${padL + i * 150 + 18}" y="42" font-size="12" fill="#e8eaed" font-family="ui-monospace,monospace">${a.arm}</text>`)
  .join("");
const svg = `<svg xmlns="http://www.w3.org/2000/svg" width="${W}" height="${H}" viewBox="0 0 ${W} ${H}" role="img">
<rect width="${W}" height="${H}" rx="12" fill="#1b1d22"/>
<text x="${padL}" y="20" font-size="14" font-weight="700" fill="#15d6c6" font-family="ui-monospace,monospace">every metric vs no-skill baseline · lower is leaner · ${R.meta.model} · ${R.meta.tasks} tasks · n=${R.meta.n}</text>
${legend}${grid}${bars}
</svg>`;
writeFileSync(join(HERE, "agentic-chart.svg"), svg);
// GitHub renders SVG-via-<img> unreliably; rasterize to PNG if a converter exists.
let img = "./benchmarks/agentic/agentic-chart.svg";
for (const cmd of [["inkscape", join(HERE, "agentic-chart.svg"), "--export-type=png", `--export-filename=${join(HERE, "agentic-chart.png")}`, "-w", "1280"], ["rsvg-convert", "-w", "1280", "-o", join(HERE, "agentic-chart.png"), join(HERE, "agentic-chart.svg")]]) {
  try { if (Bun.spawnSync(cmd).success) { img = "./benchmarks/agentic/agentic-chart.png"; break; } } catch {}
}

// Markdown table ------------------------------------------------------------
const delta = (pct: number) => (pct === 100 ? "—" : (pct < 100 ? "−" : "+") + Math.abs(pct - 100) + "%");
const rows = arms
  .filter((a) => a.arm !== "baseline")
  .map((a) => `| **${a.arm}** | ${delta(a.pct.loc)} | ${delta(a.pct.tokens)} | ${delta(a.pct.cost)} | ${delta(a.pct.sec)} |`)
  .join("\n");
const block = `<!-- AGENTIC:START -->
<!-- generated by \`bun bench:agentic\` then \`bun chart.ts\` — do not edit by hand -->

A real headless Claude Code agent editing [${R.meta.repo.split("/").slice(-1)[0]}](${R.meta.repo}), scored on the \`git diff\` it leaves. Same agent, same ${R.meta.tasks} tasks, with vs without each skill injected — **the agent's global plugins, skills and hooks are stripped per run**, so each arm sees only the one skill. ${R.meta.model}, n=${R.meta.n}.

<p align="center"><img src="${img}" alt="agentic benchmark" width="700"/></p>

| vs no-skill baseline | LOC | tokens | cost | time |
|---|---:|---:|---:|---:|
${rows}

**Reading it honestly:** this is two small one-off edits to a tiny repo — exactly the case a spec pipeline does NOT help with, and it shows. On this workload the pure-brevity skills (ponytail on LOC, caveman on tokens/time) lead; kittens-crew carries spec/ladder overhead that only pays off across many dependent tasks, which this bench doesn't exercise. Baseline means: LOC ${R.base.loc}, tokens ${R.base.tokens}, cost $${R.base.cost.toFixed(3)}, time ${R.base.sec.toFixed(0)}s. n=${R.meta.n} per cell over ${R.meta.tasks} tasks — high variance (LOC especially: the agent often makes zero edits). Directional, not a leaderboard. Re-run with more tasks and n via \`bun bench:agentic\`. We publish the unflattering run rather than fake a winning one.
<!-- AGENTIC:END -->`;

const readmePath = join(ROOT, "README.md");
const readme = readFileSync(readmePath, "utf8");
const re = /<!-- AGENTIC:START -->[\s\S]*?<!-- AGENTIC:END -->/;
writeFileSync(readmePath, re.test(readme) ? readme.replace(re, block) : readme);
console.log(re.test(readme) ? "README agentic block updated." : "No AGENTIC markers — block:\n" + block);
